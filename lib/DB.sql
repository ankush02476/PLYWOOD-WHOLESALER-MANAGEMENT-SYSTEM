//ACCOUNT//

 CREATE TABLE ACCOUNT(
     ACCOUNT_ID VARCHAR2(10)PRIMARY KEY,
     REFERENCE_ID VARCHAR2(10),
     TRANSACTION_DATE DATE DEFAULT SYSDATE,
    CONTACT_NO VARCHAR2(15),
    PARTY_NAME VARCHAR2(50)NOT NULL,
    PARTY_TYPE VARCHAR2(15)CHECK(PARTY_TYPE IN('CUSTOMER','SUPPLIER','EMPLOYEE')),
    TRANSACTION_TYPE VARCHAR2(15),
    CASHFLOW_TYPE VARCHAR2(10)CHECK(CASHFLOW_TYPE IN('IN','OUT')),
    PAYMENT_MODE VARCHAR2(15)CHECK(PAYMENT_MODE IN('CASH','BANK','UPI')),
   GST_AMOUNT NUMBER(10,2),
   GSTIN VARCHAR2(15),
   NET_AMOUNT NUMBER(10,2)NOT NULL CHECK(NET_AMOUNT>=0),
   PAID_AMOUNT NUMBER(10,2)DEFAULT 0 CHECK(PAID_AMOUNT>=0),
   PENDING_AMOUNT NUMBER(10,2)DEFAULT 0 CHECK(PENDING_AMOUNT>=0),
   PAYMENT_STATUS VARCHAR2(15)CHECK(PAYMENT_STATUS IN('PAID','PENDING','PARTIAL')),
   DUE_DATE DATE,
   CREATED_AT DATE DEFAULT SYSDATE,
   UPDATED_AT DATE
   );

 CREATE SEQUENCE ACCOUNT_SEQ
    START WITH 1
        INCREMENT BY 1
        NOCACHE
        NOCYCLE;

 CREATE OR REPLACE TRIGGER ACCOUNT_BI
    BEFORE INSERT ON ACCOUNT
    FOR EACH ROW
    BEGIN
        IF :NEW.ACCOUNT_ID IS NULL THEN
            :NEW.ACCOUNT_ID := 'ACID' || LPAD(ACCOUNT_SEQ.NEXTVAL, 6, '0');
        END IF;
    END;
    /

CREATE OR REPLACE TRIGGER trg_purchase_account
AFTER INSERT ON PURCHASE
FOR EACH ROW
BEGIN
    INSERT INTO ACCOUNT(
        TRANSACTION_DATE, PARTY_NAME, PARTY_TYPE,
        TRANSACTION_TYPE, CASHFLOW_TYPE, PAYMENT_MODE,
        GST_AMOUNT, NET_AMOUNT, PAID_AMOUNT, PENDING_AMOUNT,
        PAYMENT_STATUS, DUE_DATE, REFERENCE_ID
    )
    VALUES(
        :NEW.PURCHASE_DATE,
        :NEW.SUPPLIER_COMPANY_NAME,
        'SUPPLIER',
        'PURCHASE',
        'OUT',
        :NEW.PAYMENT_MODE,
        :NEW.TOTAL_GST,
        :NEW.NET_PURCHASE_RATE,
        :NEW.PAID_AMOUNT,
        :NEW.DUE_AMOUNT,
        CASE 
            WHEN :NEW.DUE_AMOUNT=0 THEN 'PAID'
            WHEN :NEW.PAID_AMOUNT=0 THEN 'PENDING'
            ELSE 'PARTIAL'
        END,
        :NEW.DUE_DATE,
        :NEW.PURCHASE_ID  -- use PURCHASE_ID as reference
    );
END;
/

CREATE OR REPLACE TRIGGER trg_sales_account
AFTER INSERT ON SELL
FOR EACH ROW
BEGIN
    INSERT INTO ACCOUNT(
        TRANSACTION_DATE, CONTACT_NO, PARTY_NAME, PARTY_TYPE,
        TRANSACTION_TYPE, CASHFLOW_TYPE, PAYMENT_MODE,
        GST_AMOUNT, NET_AMOUNT, PAID_AMOUNT, PENDING_AMOUNT,
        PAYMENT_STATUS, DUE_DATE, REFERENCE_ID
    )
    VALUES(
        NVL(:NEW.ORDER_DATE, SYSDATE),
        NVL(:NEW.CUSTOMER_PHONE, 'NA'),
        NVL(:NEW.CUSTOMER_NAME, 'UNKNOWN'),
        'CUSTOMER',
        'SELL',
        'IN',
        CASE 
            WHEN UPPER(:NEW.PAYMENT_MODE) IN ('CASH','BANK','UPI')
                THEN UPPER(:NEW.PAYMENT_MODE)
            ELSE 'CASH'
        END,
        NVL(:NEW.GST, 0),
        NVL(:NEW.GRAND_TOTAL, 0),
        NVL(:NEW.PAID_AMOUNT, 0),
        NVL(:NEW.DUE_AMOUNT, 0),
        CASE 
            WHEN NVL(:NEW.DUE_AMOUNT,0) = 0 THEN 'PAID'
            WHEN NVL(:NEW.PAID_AMOUNT,0) = 0 THEN 'PENDING'
            ELSE 'PARTIAL'
        END,
        NULL,
        :NEW.ORDER_ID  -- use ORDER_ID as reference
    );
END;
/

