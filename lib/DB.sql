// STOCK TABLE//

CREATE TABLE STOCK(
    STOCK_ID VARCHAR2(10) PRIMARY KEY,
    PRODUCT_ID VARCHAR2(10) NOT NULL,
    PRODUCT_NAME VARCHAR2(30),
    PRODUCT_THICKNESS VARCHAR2(10),
    STOCK_IN NUMBER(10,2) DEFAULT 0,
    STOCK_OUT NUMBER(10,2) DEFAULT 0,
    AVAILABLE_QUANTITY NUMBER(10,2) DEFAULT 0,
    MIN_STOCK_LEVEL NUMBER(10,2) DEFAULT 0,
    LAST_UPDATED DATE DEFAULT SYSDATE,
    CONSTRAINT FK_STOCK_PRODUCT FOREIGN KEY(PRODUCT_ID) REFERENCES PRODUCT(PRODUCT_ID)
);


CREATE SEQUENCE STOCK_SEQ
START WITH 1
INCREMENT BY 1
NOCACHE
NOCYCLE;


CREATE OR REPLACE TRIGGER STOCK_BI
BEFORE INSERT ON STOCK
FOR EACH ROW
BEGIN
    :NEW.STOCK_ID := 'STID' || LPAD(STOCK_SEQ.NEXTVAL,6,'0');
    :NEW.LAST_UPDATED := SYSDATE;
END;
/


CREATE OR REPLACE TRIGGER STOCK_AFTER_PURCHASE
AFTER INSERT ON PURCHASE
FOR EACH ROW
DECLARE
    v_count NUMBER;
BEGIN

    SELECT COUNT(*) INTO v_count
    FROM STOCK
    WHERE PRODUCT_ID = :NEW.PRODUCT_ID;

    IF v_count = 0 THEN

        INSERT INTO STOCK(
            PRODUCT_ID,
            PRODUCT_NAME,
            PRODUCT_THICKNESS,
            STOCK_IN,
            AVAILABLE_QUANTITY,
            MIN_STOCK_LEVEL
        )
        VALUES(
            :NEW.PRODUCT_ID,
            :NEW.PRODUCT_NAME,
            :NEW.PRODUCT_THICKNESS,
            :NEW.TOTAL_QUANTITY,
            :NEW.TOTAL_QUANTITY,
            10
        );

    ELSE

        UPDATE STOCK
        SET STOCK_IN = STOCK_IN + :NEW.TOTAL_QUANTITY,
            AVAILABLE_QUANTITY = AVAILABLE_QUANTITY + :NEW.TOTAL_QUANTITY,
            LAST_UPDATED = SYSDATE
        WHERE PRODUCT_ID = :NEW.PRODUCT_ID;

    END IF;

END;
/

